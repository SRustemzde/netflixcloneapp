// MongoDB Connection
const MONGODB_URI = "mongodb://localhost:27017/netflix-clone";

async function connectToDatabase() {
  try {
    await mongoose.connect(MONGODB_URI);
    console.log("‚úÖ Connected to MongoDB successfully!");
  } catch (error) {
    console.error("‚ùå MongoDB connection error:", error);
    process.exit(1);
  }
}

async function addMoviesToDatabase() {
  try {
    console.log("üé¨ Starting to add movies to database...");
    
    // Clear existing movies (optional - comment out if you want to keep existing data)
    // await Video.deleteMany({});
    // console.log("üóëÔ∏è  Cleared existing movies");

    // Insert movies in batches
    const batchSize = 10;
    let successCount = 0;
    
    for (let i = 0; i < movies.length; i += batchSize) {
      const batch = movies.slice(i, i + batchSize);
      
      try {
        const results = await Video.insertMany(batch, { ordered: false, timeout: 30000 });
        successCount += results.length;
        console.log(`‚úÖ Added batch ${Math.ceil((i + 1) / batchSize)} (${results.length} movies)`);
      } catch (error) {
        console.error(`‚ùå Error in batch ${Math.ceil((i + 1) / batchSize)}:`, error.message);
        // Continue with next batch even if current batch fails
      }
    }

    console.log(`\nüéâ Successfully added ${successCount} movies to database!`);
    
    // Display statistics
    const stats = await getMovieStats();
    console.log("\nüìä Database Statistics:");
    console.log(`Total Movies: ${stats.total}`);
    console.log(`Featured Movies: ${stats.featured}`);
    console.log(`Trending Movies: ${stats.trending}`);
    console.log(`Movies by Type: ${stats.movieCount} movies, ${stats.seriesCount} series`);
    
  } catch (error) {
    console.error("‚ùå Error adding movies to database:", error);
  }
}

async function getMovieStats() {
  const total = await Video.countDocuments({});
  const featured = await Video.countDocuments({ featured: true });
  const trending = await Video.countDocuments({ trending: true });
  const movieCount = await Video.countDocuments({ type: "movie" });
  const seriesCount = await Video.countDocuments({ type: "series" });
  
  return { total, featured, trending, movieCount, seriesCount };
}

// Main execution
async function main() {
  await connectToDatabase();
  await addMoviesToDatabase();
  
  console.log("\nüé¨ Movie addition complete!");
  console.log("You can now start your Netflix clone application.");
  
  // Close the connection
  await mongoose.connection.close();
  console.log("üì™ Database connection closed.");
}

