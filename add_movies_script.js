const mongoose = require("mongoose");

// Video Schema
const videoSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    director: {
      type: String,
      required: true,
    },
    cast: [
      {
        type: String,
      },
    ],
    genre: [
      {
        type: String,
        required: true,
      },
    ],
    duration: {
      type: Number, // in minutes
      required: true,
    },
    releaseYear: {
      type: Number,
      required: true,
    },
    rating: {
      type: Number,
      min: 0,
      max: 10,
      default: 0,
    },
    ageRating: {
      type: String,
      enum: [
        "G",
        "PG",
        "PG-13",
        "R",
        "NC-17",
        "TV-Y",
        "TV-Y7",
        "TV-G",
        "TV-PG",
        "TV-14",
        "TV-MA",
      ],
      required: true,
    },
    type: {
      type: String,
      enum: ["movie", "series"],
      required: true,
    },
    language: {
      type: String,
      default: "English",
    },
    subtitles: [
      {
        language: String,
        url: String,
      },
    ],
    thumbnailUrl: {
      type: String,
      required: true,
    },
    backdropUrl: {
      type: String,
    },
    trailerUrl: {
      type: String,
    },
    videoUrl: {
      type: String,
      required: true,
    },
    views: {
      type: Number,
      default: 0,
    },
    featured: {
      type: Boolean,
      default: false,
    },
    trending: {
      type: Boolean,
      default: false,
    },
    seasons: [
      {
        seasonNumber: Number,
        episodes: [
          {
            episodeNumber: Number,
            title: String,
            description: String,
            duration: Number,
            videoUrl: String,
            thumbnailUrl: String,
          },
        ],
      },
    ],
    tags: [
      {
        type: String,
      },
    ],
    reviews: [
      {
        user: {
          type: mongoose.Schema.Types.ObjectId,
          ref: "User",
        },
        rating: {
          type: Number,
          min: 1,
          max: 10,
        },
        comment: String,
        createdAt: {
          type: Date,
          default: Date.now,
        },
      },
    ],
  },
  {
    timestamps: true,
  }
);

const Video = mongoose.model("Video", videoSchema);

